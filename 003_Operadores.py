#Las cuantro lineas de arriba tienen la función de poder colocar acentos

Isuma = 20 + 23 + 44 #Creamos la variable suma y sumamos numeros para obtener 87 de resultado usando 20 y 23
print("Suma: ",Isuma) #Imprimimos la variable suma
Iresta = 20 + 23 - 130 #Creamos la variable resta y restamos numeros para obtener -87 de resultado usando 20 y 23
print("Resta: ",Iresta) #Imprimimos la variable resta
Imulti = 20 * 23 * 1.8914 #Creamos la variable multi y multiplicamos numeros para obtener 870 de resultado usando 20 y 23
print("Multiplicación: ",Imulti) #Imprimimos la variable multi
Idivi = 5000 / 230 / 2 #Creamos la variable divi y dividimos numeros para obtener 10 de resultado usando 5000 y 230
print("División: ",Idivi) #Imprimimos la variable divi
operacion = 10 / 5 + 15 - 17 #Creamos la variable operación y con sibolos básicos ordenados el resultado debe ser 0 con los numeros dados
print("Operación: ",operacion) #Imprimimos la variable operación

